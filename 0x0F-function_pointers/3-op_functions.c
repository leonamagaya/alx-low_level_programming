#include "calc.h"
/**
 * op_add - returns the sum of integers.
 *
 * @a: input integer.
 * @b: input integer.
 *
 * Return: sum of a and b.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns the difference of integers.
 *
 * @a: input integer.
 * @b: input integer.
 *
 * Return: difference of a and b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns the product of integers.
 *
 * @a: input integer.
 * @b: input integer.
 *
 * Return: product of a and b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns the result of the division of integers.
 *
 * @a: input integer.
 * @b: input integer.
 *
 * Return: quotient of dividing a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod -  returns the remainder of the division of integers.
 *
 * @a: input integer.
 * @b: input integer.
 *
 * Return: remainder of the division of a and b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
